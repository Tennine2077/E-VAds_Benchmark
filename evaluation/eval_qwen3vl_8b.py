class LocalDataset:
    """本地数据集加载器"""
    def __init__(self, video_dir, annotation_dir, parquet_name=None):
        self.video_dir = Path(video_dir)
        self.annotation_dir = Path(annotation_dir)
        self.data = self._load_annotations(parquet_name)
        
    def _load_annotations(self, parquet_name):
        """加载指定的 parquet 文件"""
        if parquet_name:
            # 加载指定的文件
            parquet_path = self.annotation_dir / parquet_name
            if not parquet_path.exists():
                # 尝试添加 .parquet 后缀
                parquet_path = self.annotation_dir / f"{parquet_name}.parquet"
            
            if not parquet_path.exists():
                raise FileNotFoundError(f"Parquet file not found: {parquet_name}")
            
            print(f"Loading annotation file: {parquet_path}")
            df = pd.read_parquet(parquet_path)
        else:
            # 如果没有指定，加载所有文件
            parquet_files = list(self.annotation_dir.glob("*.parquet"))
            if not parquet_files:
                raise FileNotFoundError(f"No parquet files found in {self.annotation_dir}")
            
            print(f"Found {len(parquet_files)} annotation files")
            dfs = [pd.read_parquet(f) for f in parquet_files]
            df = pd.concat(dfs, ignore_index=True)
        
        print(f"Total samples: {len(df)}")
        return df
    
    def get_video_path(self, video_id):
        """获取视频文件路径"""
        # 支持常见视频格式
        for ext in ['.mp4', '.avi', '.mov', '.mkv']:
            video_path = self.video_dir / f"{video_id}{ext}"
            if video_path.exists():
                return str(video_path)
        
        # 如果没有扩展名，直接尝试
        video_path = self.video_dir / video_id
        if video_path.exists():
            return str(video_path)
        
        return None
    
    def __len__(self):
        return len(self.data)
    
    def __getitem__(self, idx):
        row = self.data.iloc[idx]
        return {
            'question': row.get('question', ''),
            'answer': row.get('answer', ''),
            'ques_categories': row.get('ques_categories', ''),
            'goods_category': row.get('goods_category', ''),
            'asr_input': row.get('asr_input', ''),
            'video_id': row.get('video_id', ''),
            'ques_id': row.get('ques_id', ''),
            'evidence': row.get('evidence', ''),
            'asr_ocr_meta': row.get('asr_ocr_meta', '')
        }

def get_qwen2vl_response(model, processor, video_inputs, video_path, question, video_kwargs):
    """调用 Qwen3-VL 模型生成回答"""
    messages = [
        {
            "role": "user",
            "content": [
                {
                    "type": "video",
                    "video": video_path,
                    "total_pixels": 24576*28*28,
                    "max_frames": 768,
                },
                {"type": "text", "text": question},
            ],
        }
    ]

    text = processor.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=True
    )

    inputs = processor(
        text=[text],
        images=None,
        videos=video_inputs,
        padding=True,
        return_tensors="pt",
        **video_kwargs,
    )

    inputs = inputs.to(model.device)

    # 推理
    generated_ids = model.generate(
        **inputs,
        do_sample=True,
        temperature=1,
        max_new_tokens=1524
    )
    
    generated_ids_trimmed = [
        out_ids[len(in_ids):] for in_ids, out_ids in zip(inputs.input_ids, generated_ids)
    ]
    
    output_text = processor.batch_decode(
        generated_ids_trimmed, 
        skip_special_tokens=True, 
        clean_up_tokenization_spaces=False
    )

    return output_text[0].strip()


# LLM as Judge 的 Prompt
JUDGE_PROMPT = """
# Role
You are a professional E-commerce Content Analyst and Senior Evaluator. Your task is to objectively and rigorously assess the quality of an answer generated by a Large Language Model (LLM) based on multi-source inputs.

# Task
Compare the [Model Output] with the [Reference Answer], while considering the [Clues] and [Metadata (OCR/ASR)]. Assign a score from 0 to 1 based on the provided evaluation criteria.

# Evaluation Criteria
Please strictly follow these scoring tiers:

- **1.0 (Perfect Match)**: 
  - The answer is completely accurate and covers all key points of the reference answer.
  - Evidence cited (e.g., specific text or lines) aligns perfectly with OCR/ASR records.
  - Demonstrates deep business insight with professional terminology and logic.

- **0.75 (Accurate but Generic)**: 
  - The core answer is correct, evidence is accurate, and all key points are covered.
  - Weakness: The response is too broad or lacks the deep logic/professional E-commerce perspective found in the reference (e.g., simply saying "it's attractive" without explaining the underlying conversion logic).

- **0.5 (Partially Correct / Missing Info)**: 
  - The direction of the answer and evidence is correct, but information is incomplete.
  - Weakness: Only captures about 50% of the key points, or oversimplifies complex issues by missing important background facts from the metadata.

- **0.25 (Logical Break / Misaligned Evidence)**: 
  - Displays a clear "Right/Wrong Mismatch":
    - Case A: The cited OCR/ASR evidence is real, but the conclusion derived is entirely wrong.
    - Case B: The conclusion seems correct, but the cited evidence (timestamps, quotes, etc.) does not exist in the metadata (Hallucination).

- **0 (Completely Incorrect)**: 
  - The answer directly contradicts the OCR/ASR facts.
  - Or, the model failed to answer the question or produced severe hallucinations.

# Input Data
1. [Question]: {question}
2. [Metadata (OCR/ASR)]: {metadata}
3. [Clues]: {clues}
4. [Reference Answer]: {reference_answer}
5. [Model Output]: {model_output}

# Output Template
Answer: <Only the numeric value: 0, 0.25, 0.5, 0.75, or 1.
# Output Template
Answer: <Only the numeric value: 0, 0.25, 0.5, 0.75, or 1.0>

# Constraints
- DO NOT include any introductory text.
- DO NOT provide a range. You must choose ONE exact value from the scale.
- The Answer line must contain "Answer: " and the number.
"""


def get_llm_judge_score(client, question, metadata, clues, reference_answer, model_output, max_retries=10):
    """使用 LLM 作为评判器打分"""
    
    # 提取模型输出中的答案部分
    judge_response = model_output
    if "<answer>" in judge_response and "</answer>" in judge_response:
        judge_response = judge_response.split("<answer>")[1].split("</answer>")[0]
    
    # 构造提示词
    prompt = JUDGE_PROMPT.format(
        metadata=metadata,
        question=question,
        clues=clues,
        reference_answer=reference_answer,
        model_output=judge_response
    )
    
    messages = [{"role": "user", "content": prompt}]
    
    # 重试机制
    for retry in range(max_retries):
        try:
            completion = client.chat.completions.create(
                model="qwen-plus",  # 改为你可用的模型
                messages=messages,
                max_tokens=8000,
                temperature=0.0,
                seed=42,
                timeout=150,
            )
            
            response = completion.choices[0].message.content
            score_text = response.split('Answer:')[-1].split("\n")[0].strip()
            score = float(score_text)
            
            return score
            
        except Exception as e:
            print(f"Judge API error (retry {retry+1}/{max_retries}): {e}")
            time.sleep(5)
    
    print("Judge API failed after all retries")
    return None


def evaluate_model(args):
    """主评测函数"""
    
    # 设置设备
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"Using device: {device}")
    
    # 加载数据集
    print("Loading dataset...")
    dataset = LocalDataset(args.video_dir, args.annotation_dir, args.parquet_name)
    
    # 加载模型
    print(f"Loading model: {args.model_name}")
    model = Qwen3VLForConditionalGeneration.from_pretrained(
        args.model_name,
        torch_dtype=torch.bfloat16,
        attn_implementation="flash_attention_2",
        device_map=device
    )
    processor = AutoProcessor.from_pretrained(
        args.model_name,
        trust_remote_code=True,
        use_fast=True
    )
    
    # 初始化 LLM Judge
    client = OpenAI(
        api_key=args.judge_api_key,
        base_url=args.judge_base_url
    )
    
    # 创建输出目录
    os.makedirs(args.output_dir, exist_ok=True)
    
    # 结果存储
    results = []
    failed_videos = []
    
    # 视频缓存
    last_video_path = ""
    video_inputs = None
    video_kwargs = None
    
    print("Starting evaluation...")
    for idx in tqdm(range(len(dataset))):
        sample = dataset[idx]
        
        # 获取视频路径
        video_path = dataset.get_video_path(sample['video_id'])
        if video_path is None:
            print(f"Video not found: {sample['video_id']}")
            failed_videos.append(sample['video_id'])
            continue
        
        try:
            # 处理视频输入（缓存机制，避免重复处理同一视频）
            if last_video_path != video_path:
                messages = [
                    {
                        "role": "user",
                        "content": [
                            {
                                "type": "video",
                                "video": video_path,
                                "total_pixels": 24576*28*28,
                                "max_frames": 768,
                            },
                        ],
                    }
                ]
                _, video_inputs, video_kwargs = process_vision_info(
                    messages,
                    image_patch_size=16,
                    return_video_kwargs=True,
                    return_video_metadata=True
                )
                last_video_path = video_path
            
            # 构造问题
            asr = sample['asr_input']
            question = sample['question']
            input_question = f"{asr}\n\nBy watching the video, you are required to answer a question within 50 words: Question: {question}"
            
            # 模型推理
            with torch.no_grad():
                prediction = get_qwen2vl_response(
                    model, processor, video_inputs, 
                    video_path, input_question, video_kwargs
                )
            
            # 记录结果
            result = {
                'video_id': sample['video_id'],
                'ques_id': sample['ques_id'],
                'question': question,
                'answer': sample['answer'],
                'prediction': prediction,
                'ques_categories': sample['ques_categories'],
                'goods_category': sample['goods_category'],
                'asr_input': asr,
                'evidence': sample['evidence'],
                'asr_ocr_meta': sample['asr_ocr_meta']
            }
            
            # LLM Judge 打分（可选）
            if client:
                score_R5 = get_llm_judge_score(
                    client,
                    question=question,
                    metadata=sample['asr_ocr_meta'],
                    clues=sample['evidence'],
                    reference_answer=sample['answer'],
                    model_output=prediction
                )
                
                if score_R5 is not None:
                    result['score_R5'] = score_R5
                    
                    # R3 评分（3档）
                    score_R3 = score_R5
                    if score_R5 == 0.75:
                        score_R3 = 0.5
                    if score_R5 == 0.25:
                        score_R3 = 0
                    result['score_R3'] = score_R3
                    
                    # R2 评分（2档）
                    score_R2 = 1.0 if score_R5 == 1.0 else 0.0
                    result['score_R2'] = score_R2
                else:
                    result['score_R5'] = None
                    result['score_R3'] = None
                    result['score_R2'] = None
            
            results.append(result)
            
            # 定期保存
            if (idx + 1) % 10 == 0:
                df_temp = pd.DataFrame(results)
                df_temp.to_csv(
                    f"{args.output_dir}/results_checkpoint_{idx+1}.csv",
                    index=False
                )
            
            # 清理显存
            torch.cuda.empty_cache()
            
        except Exception as e:
            print(f"Error processing {sample['video_id']}: {e}")
            failed_videos.append(sample['video_id'])
            continue
    
    # 保存最终结果
    print("\nSaving results...")
    df_results = pd.DataFrame(results)
    df_results.to_csv(
        f"{args.output_dir}/final_results.csv",
        index=False
    )
    df_results.to_json(
        f"{args.output_dir}/final_results.json",
        orient='records',
        indent=2
    )
    
    # 保存失败的视频列表
    if failed_videos:
        with open(f"{args.output_dir}/failed_videos.txt", 'w') as f:
            f.write('\n'.join(failed_videos))
        print(f"\nFailed videos: {len(failed_videos)}")
        print(f"See {args.output_dir}/failed_videos.txt for details")
    
    # 计算统计信息
    print("\n" + "="*50)
    print("Evaluation Summary")
    print("="*50)
    print(f"Total samples: {len(dataset)}")
    print(f"Successfully processed: {len(results)}")
    print(f"Failed: {len(failed_videos)}")
    
    if client and 'score_R5' in df_results.columns:
        valid_scores = df_results['score_R5'].dropna()
        if len(valid_scores) > 0:
            print(f"\nAverage Score (R5): {valid_scores.mean():.3f}")
            print(f"Average Score (R3): {df_results['score_R3'].dropna().mean():.3f}")
            print(f"Average Score (R2): {df_results['score_R2'].dropna().mean():.3f}")
            print("\nScore Distribution (R5):")
            print(df_results['score_R5'].value_counts().sort_index())
    
    print(f"\nResults saved to: {args.output_dir}")


def parse_args():
    parser = argparse.ArgumentParser(description='Local Video QA Evaluation')
    
    # 数据路径
    parser.add_argument('--video_dir', type=str, default='../videos',
                        help='Directory containing video files')
    parser.add_argument('--annotation_dir', type=str, default='../annotations',
                        help='Directory containing parquet annotation files')
    parser.add_argument('--output_dir', type=str, default='./results',
                        help='Output directory for results')
    parser.add_argument('--parquet_name', type=str, default='EVAds_TEST.parquet',
                        help='Specific parquet file name to evaluate (e.g., "annotation_part1.parquet")')
    
    # 模型配置
    parser.add_argument('--model_name', type=str, 
                        default='Qwen/Qwen3-VL-8B-Instruct',
                        help='Model name or path')
    
    # LLM Judge 配置
    parser.add_argument('--judge_api_key', type=str, default='',
                        help='API key for judge LLM')
    parser.add_argument('--judge_base_url', type=str, default='',
                        help='Base URL for judge LLM API')
    
    # 其他配置
    parser.add_argument('--world_size', type=int, default=1,
                        help='Number of processes (for future multi-GPU support)')
    parser.add_argument('--batch_size', type=int, default=1,
                        help='Batch size (currently only supports 1)')
    
    args = parser.parse_args()
    return args


if __name__ == "__main__":
    args = parse_args()
    evaluate_model(args)
